export function getLalitaNameByNumber(number: number): SahasranamaName | undefined {
  return LALITA_SAHASRANAMA.find((name) => name.number === number);
}

export function getShivaNameByNumber(number: number): SahasranamaName | undefined {
  return SHIVA_SAHASRANAMA.find((name) => name.number === number);
}

export function searchLalitaNames(query: string): SahasranamaName[] {
  const lowerQuery = query.toLowerCase();
  return LALITA_SAHASRANAMA.filter(
    (name) =>
      name.sanskrit.includes(lowerQuery) ||
      name.transliteration.includes(lowerQuery) ||
      name.meaning.toLowerCase().includes(lowerQuery)
  );
}

export function searchShivaNames(query: string): SahasranamaName[] {
  const lowerQuery = query.toLowerCase();
  return SHIVA_SAHASRANAMA.filter(
    (name) =>
      name.sanskrit.includes(lowerQuery) ||
      name.transliteration.includes(lowerQuery) ||
      name.meaning.toLowerCase().includes(lowerQuery)
  );
}
