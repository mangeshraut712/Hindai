export function getTranslation(id: string, language: Language): Translation | undefined {
  return TRANSLATIONS.find((t) => t.id === id && t.language === language);
}

export function getTranslationsByLanguage(language: Language): Translation[] {
  return TRANSLATIONS.filter((t) => t.language === language);
}

export function getTranslationsById(id: string): Translation[] {
  return TRANSLATIONS.filter((t) => t.id === id);
}

export function searchTranslations(query: string, language?: Language): Translation[] {
  const lowerQuery = query.toLowerCase();
  let results = TRANSLATIONS.filter(
    (t) =>
      t.sanskrit.includes(lowerQuery) ||
      t.transliteration.includes(lowerQuery) ||
      t.translation.toLowerCase().includes(lowerQuery)
  );

  if (language) {
    results = results.filter((t) => t.language === language);
  }

  return results;
}
